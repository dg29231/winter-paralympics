function setup() {
  let container = document.getElementById('sketch');
  let w = container.offsetWidth;
  let h = container.offsetHeight || 932;
  
  let canvas = createCanvas(w, h);
  canvas.parent('sketch');
  angleMode(DEGREES);

  picker = createSelect();
  picker.parent('sketch');
  picker.position(115,720);
  for(let i = 0; i < auswahl_land.length; i++){
    picker.option([auswahl_land[i]])
  }
  picker.class("style-picker")
  picker.changed(kreise)

  noLoop();

}

function draw() {
  kreise();
}

function sortByYear(data, year) {
  data.sort((a, b) => {
    return b[year] - a[year];
  });
  return data;
} 

function kreise (){
  background("#454073");

  for (let j = 0; j < 13; j++) {

    data_sorted = sortByYear(data, years_med[currentYear]);
    console.log("sorted", data_sorted);

    for (let i = 0; i < 5; i++) {
      let df = data_sorted[i];

      size_med = scale(df[years_med[currentYear]]);
      size_part = scale(df[years_part[currentYear]]);


      let dx = (d - dStep) / 2 * cos(winkel); // Berechnung Punktposition d/2 ist der Radius vom Umfang des Kreises
      let dy = (d - dStep) / 2 * sin(winkel);


      // fill(0)
      // circle(width / 2, height / 2, 10); //Raster

      opacity(df.country, df.europa);
      noStroke();
      circle(width / 2 + dx, height / 2 + dy, size_part);

      opacityStroke(df.country)
      noFill();
      circle(width / 2 + dx, height / 2 + dy, size_med)


      // circle(width / 2, height / 2, 120); //Raster
      // circle(width / 2, height / 2, 360); //Raster

      dStep = dStep + 60;
      console.log(years[currentYear])
    }

    dStep = 0
    d = 360;
    winkel = winkel + (360/13)
    currentYear = currentYear+1
    if(currentYear >= 13){
      currentYear = 0;
    }

  }
}

function opacity (country, europa){
  land = picker.value();

  if(land === "China"){
    land = "People's Republic of China";
  }
  if(land === "Russia"){
    land = "Russian Federation"
  }
  if(land === "USA"){
    land = "United States of America";
  }

  console.log(land)

  if(land === "Top 5 Nationen" && europa === "True"){
    fill("rgba(151, 141, 183, 0.5)")
  }else if(land === "Top 5 Nationen" && europa === "False"){
    fill("rgba(205, 255, 148, 0.5)")
  }else if(land === country && europa === "True"){
    fill("rgba(151, 141, 183, 0.5)")
  }else if(land === country && europa === "False"){
    fill("rgba(205, 255, 148, 0.5)")
  }else if(land != country && europa === "True"){
    fill("rgba(151, 141, 183, 0.1)")
  }else if(land != country && europa === "False"){
    fill("rgba(205, 255, 148, 0.1)")
  }
}

